def generate_insert_sql(product_data):
    sql_template = """WITH product_row AS (
        INSERT INTO products(
            name, 
            price, 
            stock_count, 
            available_stock_count, 
            short_description, 
            long_description,
            avg_rating, 
            rating_count, 
            img_link, 
            category_id
        ) VALUES 
            (
                '{name}', 
                {price}, 
                {stock_count}, 
                {available_stock_count}, 
                '{short_description}', 
                '{long_description}',
                {avg_rating}, 
                {rating_count}, 
                '{img_link}', 
                {category_id}
            )
        RETURNING id
    )
    INSERT INTO product_categories(product_id, category_id)
    VALUES
        ((SELECT id FROM product_row), {category_id});"""
    sql = sql_template.format(**product_data)
    sql = sql.replace('"', "'")
    return sql

